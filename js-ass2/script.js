// Object operations

// creating empty object
var person = {};

// adding properties to objects
person["name"] = "John";
person["age"] = 30;
person["city"] = "New York";

// Removing an property to object
delete person.age;

// Adding new property
person["Job"] = "Engineer";

// update property
person["city"] = "San Francisco";

// printing the object
console.log(person);